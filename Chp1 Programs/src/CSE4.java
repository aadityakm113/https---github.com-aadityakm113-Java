
import java.util.*;
public class CSE4 {
    public static void main(String[] args)
    {
        Scanner sc= new Scanner(System.in);
        int x= sc.nextInt();

        switch(x)
        {
            case 1:
                System.out.println("AAA");
                break;
            case 2:
                System.out.println("BAA");
                break;
            case 3:
                System.out.println("CAA");
                break;
            default:
                System.out.println("JAVA");
                break;
        }
            
        sc.close();



    }
}
