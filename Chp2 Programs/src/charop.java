//Program that performs arithmetic operations with values of type char. 

public class charop {
    public static void main(String[] args)
    {
        char c1='a',c2='c';
        System.out.println(c1+c2);
        System.out.println(c1-c2);
        System.out.println(c1*c2);
        System.out.println(c1/c2);
    }
    
}
